
dia = int(input("Informe o dia da semana entre 1 e 7: "))

if dia == 1:
    print("Segunda")
elif dia == 2:
    print("Terça") 
elif dia == 3:
    print("Quarta") 
elif dia == 4:
    print("Quinta") 
elif dia == 5:
    print("Sexta") 
elif dia == 6:
    print("Sabado") 
elif dia == 7:
    print("Domingo") 
else:
    print("Erro. Digite um número entre 1 e 7.") 